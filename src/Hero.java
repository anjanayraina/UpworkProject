public class Hero {
  // positions of Hero
    int posX;
    int posY;
    Hero(){
        this.posX = 0;
        this.posY=0;
    }
    // getter and setter functions
    public int getPosY() {
        return posY;
    }

    public void setPosY(int posY) {
        this.posY = posY;
    }

    public int getPosX() {
        return posX;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }
}
